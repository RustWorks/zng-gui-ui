/// Sets the default properties and when conditions on an widget.
///
/// # Example
/// 
/// ```
/// # use zero_ui_core::{*, widget_base::*};
/// #[widget($crate::Wgt)]
/// pub struct Wgt(WidgetBase);
/// impl Wgt {
///     #[widget(on_start)]
///     fn on_start(&mut self) {
///         defaults! {
///             self;
///             enabled = false;
///         }
///     }
/// }
/// ```
#[macro_export]
macro_rules! defaults {
    (
        $wgt_borrow_mut:expr;
        $($tt:tt)*
    ) => {
        $crate::widget_new! {
            start { {
                let mut wgt__ = $wgt_borrow_mut;
                wgt__.start_defaults();
                wgt__
            } }
            end { wgt__.end_defaults() }
            new { $($tt)* }
        }
    }
}

/// Implement a property on the widget to strongly associate it with the widget.
/// 
/// This is equivalent of the `impl(Widget)` directive in the [`property`] macro, but generates
#[macro_export]
macro_rules! impl_properties {
    (
        $(
            $(#[$attr:meta])*
            $vis:vis fn $($property:ident)::+ ($($arg:ident : $arg_ty:ty)*);            
        )+
    ) => {
        $(
            $crate::property_impl! {
                attrs { $(#[$attr])* }
                vis { $vis }
                path { $($property)::* }
                args { $($arg:$arg_ty),* }
            }
        )+
    }
}